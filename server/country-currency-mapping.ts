export function getCurrencyForCountry(countryCode: string) {
  const mapping = COUNTRY_CURRENCY_MAP[countryCode as keyof typeof COUNTRY_CURRENCY_MAP];
  return mapping || COUNTRY_CURRENCY_MAP.DEFAULT;
}

export function getAllCountries() {
  return Object.entries(COUNTRY_CURRENCY_MAP)
    .filter(([code]) => code !== 'DEFAULT')
    .map(([code, data]) => ({
      code,
      name: data.country,
      currency: data.currency,
      currencyName: data.name,
      supportedCurrencies: data.supportedCurrencies
    }))
    .sort((a, b) => a.name.localeCompare(b.name));
}

